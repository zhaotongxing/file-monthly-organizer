def is_rtl(lang: str) -> bool:
    """是否为从右到左(RTL)语言"""
    return UN_LANGUAGES.get(lang, {}).get("rtl", False)


class I18n:
    """国际化工具类（单例）"""
    _instance = None
    _lang = DEFAULT_LANG

    def __new__(cls):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
            cls._instance._lang = DEFAULT_LANG
            cls._instance._data = LANGUAGES[DEFAULT_LANG]
        return cls._instance

    def set_language(self, lang: str):
        """切换语言"""
        if lang in LANGUAGES:
            self._lang = lang
            self._data = LANGUAGES[lang]
            return True
        return False

    @property
    def lang(self) -> str:
        return self._lang

    def get(self, key: str, **kwargs) -> str:
        """获取翻译文本并格式化"""
        text = getattr(self._data, key, key)
        if kwargs:
            try:
                return text.format(**kwargs)
            except (KeyError, ValueError):
                return text
        return text

    # 兼容属性访问
    def __getattr__(self, key: str):
        val = getattr(self._data, key, key)
        if isinstance(val, str):
            class LazyFormat:
                def __init__(self, text):
                    self.text = text
                def __str__(self):
                    return self.text
                def format(self, **kwargs):
                    try:
                        return self.text.format(**kwargs)
                    except:
                        return self.text
            return LazyFormat(val)
        return val


# 便捷函数
def set_language(lang: str) -> bool:
    """设置全局语言"""
    return I18n().set_language(lang)


def get_current() -> I18n:
    """获取当前 i18n 实例"""
    return I18n()
